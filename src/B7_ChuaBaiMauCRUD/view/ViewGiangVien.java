    private void showDataTable(List<GiangVien> listGiangVien) {
        dtm.setRowCount(0);
        for (GiangVien gv : listGiangVien) {
            dtm.addRow(new Object[]{gv.getMa(), gv.getTen(), gv.getLoai(), gv.getTuoi(), gv.getBac(), gv.getGioiTinh()});
        }
    }

